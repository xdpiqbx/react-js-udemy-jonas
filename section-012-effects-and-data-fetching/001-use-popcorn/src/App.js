import { useEffect, useState } from "react";
import StarRating from "./StarRating.js";
import tempMovieData from "./data/tempMovieData.js";
import tempWatchedData from "./data/tempWatchedData.js";

import vars from "./config.js";

const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

export default function App() {
  const [query, setQuery] = useState("");
  const [movies, setMovies] = useState(tempMovieData);
  const [watched, setWatched] = useState(tempWatchedData);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  function handleSelectMovie(id) {
    setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  function handleCloseMovie() {
    setSelectedId(null);
  }

  useEffect(() => {
    async function fetchMovies() {
      try {
        setIsLoading(true);
        setError("");
        const fetchResult = await fetch(
          `http://www.omdbapi.com/?apikey=` + vars.OMDb_API_KEY + `&s=${query}`
        );
        if (!fetchResult.ok) {
          throw new Error("Something went wrong with fetching movies");
        }
        const responseJson = await fetchResult.json();
        if (responseJson.Response === "False") {
          throw new Error(responseJson.Error);
        }
        setMovies(responseJson.Search);
      } catch (error) {
        console.log(error.message);
        setError(error.message);
      } finally {
        setIsLoading(false);
      }
    }
    if (query.length < 3) {
      setMovies([]);
      setError("");
      return;
    }
    fetchMovies();
  }, [query]);

  return (
    <>
      <NavBar>
        <Logo />
        <SearchBar query={query} setQuery={setQuery} />
        <NumResults numberOfMovies={movies.length} />
      </NavBar>
      <MainContent>
        <Box>
          {isLoading && <Loader />}
          {error && <ErrorMessage message={error} />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
        </Box>
        <Box>
          {selectedId && (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
            />
          )}
          {!selectedId && <WatchedSummary watched={watched} />}
          {!selectedId && <WatchedMoviesList watched={watched} />}
        </Box>
      </MainContent>
    </>
  );
}

function Loader() {
  return <p className="loader">Loading...</p>;
}

